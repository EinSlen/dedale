package dedale.elements;
/**
 * Une énumération correspondant aux différentes directions possibles.
 * @author Jean-François Condotta
 * @since 1.0 (22/02/23)
 * @version 1.0 (22/02/23)
 */
public enum Direction {
	/** 
	 * Direction du bas vers le haut. 
	 */
	HAUT,
	/** 
	 * Direction de la gauche vers la droite.
	 */
	DROITE,
	/** 
	 * Direction du haut vers le bas.
	 */
	BAS,
	/** 
	 * Direction de la droite vers la gauche. *
	 */
	GAUCHE
}
